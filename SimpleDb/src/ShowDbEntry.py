class ShowDbEntry:
    def __init__(self,
                 date=1,
                 showTitle='Show Title',
                 genre='Action',                                 
                 status='Currently Airing',
                 rating='G',
                 star_rating='1.0'):
        self.date = date
        self.showTitle = showTitle
        self.genre = genre
        self.status = status
        self.rating = rating
        self.star_rating = star_rating
